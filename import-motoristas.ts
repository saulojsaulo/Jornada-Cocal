async function run() {
  const lines = tsvData.split('\n');
  let successCount = 0;
  
  for (const line of lines) {
    if (!line.trim()) continue;
    
    // Some lines might not have 4 columns, or phone might be 0
    const parts = line.split('\t');
    const nome = parts[0]?.trim();
    const cpf = parts[1]?.trim();
    const telefone = parts[2]?.trim();
    const senha = parts[3]?.trim();
    
    if (!nome || !senha) continue;
    
    // Upsert logic: if motorista with this senha exists, update it. Else insert.
    // Wait, since we added the placeholders ("Motorista 327000"), they ALREADY EXIST by senha!
    // So we can just try to update by senha first.
    
    console.log(`Processing: ${nome} (Senha: ${senha})`);
    
    const cleanTel = telefone === '0' || !telefone ? null : telefone;
    const cleanCpf = cpf === '0' || !cpf ? null : cpf;
    
    const { data: existing, error: findErr } = await supabase
      .from('motoristas')
      .select('id')
      .eq('senha', senha)
      .maybeSingle();

    if (existing) {
      // Update existing
      const { error: updateErr } = await supabase
        .from('motoristas')
        .update({
          nome,
          cpf: cleanCpf,
          telefone: cleanTel,
        })
        .eq('id', existing.id);
        
      if (updateErr) {
        console.error(`Failed to update ${senha}: ${updateErr.message}`);
      } else {
        successCount++;
      }
    } else {
      // Insert new
      const { error: insertErr } = await supabase
        .from('motoristas')
        .insert({
          nome,
          cpf: cleanCpf,
          telefone: cleanTel,
          senha,
          ativo: true
        });
        
      if (insertErr) {
        console.error(`Failed to insert ${senha}: ${insertErr.message}`);
      } else {
        successCount++;
      }
    }
  }
  
  console.log(`\nImport complete! Successfully processed ${successCount}/${lines.length} motoristas.`);
}

